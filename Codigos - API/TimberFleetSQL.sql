CREATE TABLE TimberFleet_Contadores (
	id_interno int IDENTITY(1,1) NOT NULL UNIQUE,
	id_ident nvarchar(100) NOT NULL,
	prefixo nvarchar(8) NOT NULL,
	nome_operador nvarchar(100),
	data_contadores datetime NOT NULL,
	operacao_status nvarchar(30),
	odometro_motor float(53) NOT NULL,
	odometro_operacao float(53) NOT NULL,
	odometro_implemento float(53) NOT NULL,
	odometro_rodante float(53) NOT NULL,
	horimetro_motor float(53) NOT NULL,
	horimetro_operacao float(53) NOT NULL,
	horimetro_implemento float(53) NOT NULL,
	horimetro_rodante float(53) NOT NULL,
	ativacao_motor int NOT NULL,
	ativacao_operacao int NOT NULL,
	ativacao_implemento int NOT NULL,
	ativacao_rodante int NOT NULL,
	tempo_movimentacao_motor float(53) NOT NULL,
	tempo_movimentacao_operacao float(53) NOT NULL,
	tempo_movimentacao_implemento float(53) NOT NULL,
	tempo_movimentacao_rodante float(53) NOT NULL,
	diferenca_odometro_motor FLOAT(53) NOT NULL,
	diferenca_odometro_operacao FLOAT(53) NOT NULL,
	diferenca_odometro_implemento FLOAT(53) NOT NULL,
	diferenca_odometro_rodante FLOAT(53) NOT NULL,
	diferenca_horimetro_motor FLOAT(53) NOT NULL,
	diferenca_horimetro_operacao FLOAT(53) NOT NULL,
	diferenca_horimetro_implemento FLOAT(53) NOT NULL,
	diferenca_horimetro_rodante FLOAT(53) NOT NULL,
	diferenca_ativacao_motor INT NOT NULL,
	diferenca_ativacao_operacao INT NOT NULL,
	diferenca_ativacao_implemento INT NOT NULL,
	diferenca_ativacao_rodante INT NOT NULL,
	diferenca_tempo_movimentacao_motor FLOAT(53) NOT NULL,
	diferenca_tempo_movimentacao_operacao FLOAT(53) NOT NULL,
	diferenca_tempo_movimentacao_implemento FLOAT(53) NOT NULL,
	diferenca_tempo_movimentacao_rodante FLOAT(53) NOT NULL,
	data_registro date NOT NULL,
	PRIMARY KEY (id_interno)
);

