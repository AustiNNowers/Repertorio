DROP TABLE IF EXISTS TimberFleet_Contadores;
DROP TABLE IF EXISTS TimberFleet_Apontamentos;
DROP TABLE IF EXISTS TimberFleet_Formulario;
DROP TABLE IF EXISTS TimberFleet_Localizacao;
DROP TABLE IF EXISTS TimberFleet_Producao;
